#!/usr/bin/env node
import { createClient } from "@supabase/supabase-js";
import crypto from "node:crypto";
import fs from "node:fs";
import path from "node:path";

const url = process.env.NEXT_PUBLIC_SUPABASE_URL;
const serviceKey = process.env.SUPABASE_SERVICE_ROLE_KEY;

if (!url || !serviceKey) {
  console.error("Missing NEXT_PUBLIC_SUPABASE_URL or SUPABASE_SERVICE_ROLE_KEY");
  process.exit(1);
}

const supabase = createClient(url, serviceKey, {
  auth: { autoRefreshToken: false, persistSession: false },
});

const rolesToCreate = ["admin", "teacher", "student", "parent"];
const runId = crypto.randomBytes(4).toString("hex");

function randomPassword() {
  return `Test${crypto.randomBytes(4).toString("hex")}A1`;
}

function envKey(role) {
  return role.toUpperCase();
}

async function ensureRole(roleName) {
  const { data, error } = await supabase.from("roles").select("id, name").eq("name", roleName).single();
  if (error || !data) {
    throw new Error(`Role "${roleName}" not found in roles table`);
  }
  return data.id;
}

async function createUserForRole(roleName, roleId) {
  const email = `test.${roleName}.${runId}@example.com`;
  const password = randomPassword();
  const fullName = `Test ${roleName[0].toUpperCase()}${roleName.slice(1)} ${runId}`;

  const { data: authData, error: authError } = await supabase.auth.admin.createUser({
    email,
    password,
    email_confirm: true,
    user_metadata: { full_name: fullName },
  });

  if (authError || !authData?.user?.id) {
    throw new Error(`Failed to create ${roleName} test user: ${authError?.message || "unknown"}`);
  }

  const userId = authData.user.id;

  const { error: profileError } = await supabase.from("profiles").upsert(
    {
      id: userId,
      email,
      full_name: fullName,
      phone: null,
      avatar_url: null,
    },
    { onConflict: "id" }
  );
  if (profileError) {
    throw new Error(`Failed to upsert profile for ${roleName}: ${profileError.message}`);
  }

  const { error: roleError } = await supabase.from("user_roles").insert({
    user_id: userId,
    role_id: roleId,
  });
  if (roleError) {
    throw new Error(`Failed to assign role ${roleName}: ${roleError.message}`);
  }

  return { email, password };
}

async function main() {
  const outPath = path.resolve(process.cwd(), ".test-users.env");
  const envLines = [
    `# Auto-generated by scripts/bootstrap-test-users.mjs`,
    `# Generated at: ${new Date().toISOString()}`,
  ];

  for (const role of rolesToCreate) {
    const roleId = await ensureRole(role);
    const creds = await createUserForRole(role, roleId);
    const key = envKey(role);
    envLines.push(`TEST_${key}_EMAIL=${creds.email}`);
    envLines.push(`TEST_${key}_PASSWORD=${creds.password}`);
  }

  fs.writeFileSync(outPath, `${envLines.join("\n")}\n`, "utf8");
  console.log(`Created test users and wrote credentials to ${outPath}`);
}

main().catch((err) => {
  console.error(err.message || err);
  process.exit(1);
});
